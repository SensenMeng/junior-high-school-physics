async def generate_sample_data():
    """生成并导入示例教材数据"""
    print("📚 开始生成示例教材数据（人教版2024八年级上册物理）...")

    chapter_col = get_chapter_collection()
    sentence_col = get_sentence_collection()
    keyword_col = get_keyword_collection()

    all_keywords = set()

    # 1. 章节级
    print("\n📖 生成章节级数据...")
    chapter_entries = []
    for ch in SAMPLE_CHAPTERS:
        for sec in ch["sections"]:
            ch_id = str(uuid.uuid4())
            # 用章节前150字作为摘要
            summary = sec["content"][:150] + "..."
            chapter_entries.append({
                "id": ch_id,
                "text": summary,
                "metadata": {
                    "title": f"{ch['title']} - {sec['title']}",
                    "type": "chapter",
                    "chapter": ch["title"],
                    "section": sec["title"],
                    "textbook": "人教版物理2024八年级上册",
                },
            })

    if chapter_entries:
        texts = [e["text"] for e in chapter_entries]
        embeddings = await get_embeddings(texts)
        chapter_col.add(
            ids=[e["id"] for e in chapter_entries],
            embeddings=embeddings,
            documents=[e["text"] for e in chapter_entries],
            metadatas=[e["metadata"] for e in chapter_entries],
        )
        print(f"   ✅ 已导入 {len(chapter_entries)} 个章节摘要")
        await asyncio.sleep(5)

    # 2. 句子级
    print("\n📝 生成句子级数据...")
    sentence_entries = []
    for ch in SAMPLE_CHAPTERS:
        for sec in ch["sections"]:
            # 定义
            for term, def_text in sec["definitions"]:
                sent_id = str(uuid.uuid4())
                sentence_entries.append({
                    "id": sent_id,
                    "text": f"{term}：{def_text}",
                    "metadata": {
                        "type": "definition",
                        "chapter_title": ch["title"],
                        "section_title": sec["title"],
                        "textbook": "人教版物理2024八年级上册",
                        "source": sec["content"][:200],
                    },
                })
            # 公式
            for formula in sec["formulas"]:
                sent_id = str(uuid.uuid4())
                sentence_entries.append({
                    "id": sent_id,
                    "text": formula,
                    "metadata": {
                        "type": "formula",
                        "chapter_title": ch["title"],
                        "section_title": sec["title"],
                        "textbook": "人教版物理2024八年级上册",
                    },
                })
            # 实验
            for exp in sec["experiments"]:
                sent_id = str(uuid.uuid4())
                sentence_entries.append({
                    "id": sent_id,
                    "text": f"实验：{exp}",
                    "metadata": {
                        "type": "experiment",
                        "chapter_title": ch["title"],
                        "section_title": sec["title"],
                        "textbook": "人教版物理2024八年级上册",
                    },
                })

    if sentence_entries:
        batch_size = 16
        for i in range(0, len(sentence_entries), batch_size):
            batch = sentence_entries[i : i + batch_size]
            embeddings = await get_embeddings([e["text"] for e in batch])
            sentence_col.add(
                ids=[e["id"] for e in batch],
                embeddings=embeddings,
                documents=[e["text"] for e in batch],
                metadatas=[e["metadata"] for e in batch],
            )
            print(f"   ✅ 已导入句子 {i+1}-{i+len(batch)}/{len(sentence_entries)}")
            if i + batch_size < len(sentence_entries):
                await asyncio.sleep(3)
        print(f"   ✅ 共导入 {len(sentence_entries)} 个句子")
        await asyncio.sleep(5)

    # 3. 关键词级
    print("\n🔑 生成关键词级数据...")
    for ch in SAMPLE_CHAPTERS:
        for sec in ch["sections"]:
            for kw in sec["keywords"]:
                all_keywords.add(kw)

    keyword_entries = []
    for kw in sorted(all_keywords):
        kw_id = str(uuid.uuid4())
        keyword_entries.append({
            "id": kw_id,
            "text": kw,
            "metadata": {
                "textbook": "人教版物理2024八年级上册",
            },
        })

    if keyword_entries:
        chunk_size = 50
        for i in range(0, len(keyword_entries), chunk_size):
            chunk = keyword_entries[i : i + chunk_size]
            embeddings = await get_embeddings([e["text"] for e in chunk])
            keyword_col.add(
                ids=[e["id"] for e in chunk],
                embeddings=embeddings,
                documents=[e["text"] for e in chunk],
                metadatas=[e["metadata"] for e in chunk],
            )
        print(f"   ✅ 已导入 {len(keyword_entries)} 个关键词")

    print(f"\n🎉 示例数据生成完成!")
    print(f"   - 章节摘要: {len(chapter_entries)} 条")
    print(f"   - 句子/定义/公式/实验: {len(sentence_entries)} 条")
    print(f"   - 关键词: {len(keyword_entries)} 个")


async def main():
    print("=" * 50)
    print("人教版2024八年级上册物理 - 示例数据导入")
    print("=" * 50)
    await generate_sample_data()


if __name__ == "__main__":
    asyncio.run(main())
